const map = {
    "*": "\\*",
    "#": "\\#",
    "(": "\\(",
    ")": "\\)",
    "[": "\\[",
    "]": "\\]",
    "_": "\\_",
    "\\": "\\\\",
    "+": "\\+",
    "-": "\\-",
    "<": "&lt;",
    ">": "&gt;",
    "&": "&amp;",
    "=": "\\=",
    "|": "&#124;",
    ".": "\\.",
};

const mdEscape = (str) => str?.replace(/[|*()\[\]+\-\\_.=#<>]/g, m => map[m]);

module.exports = { mdEscape };